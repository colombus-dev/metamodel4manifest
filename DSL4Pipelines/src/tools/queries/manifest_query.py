"""This module defines the ManifestQuery class, which provides methods to query a Manifest object.
The ManifestQuery class allows users to retrieve artefacts, metrics, and input data from a manifest."
The aim of this class is to serve as an interface to access and filter the components of a manifest in a structured way,
enabling users to easily extract relevant information based on specific criteria."""

from typing import Optional

from DSL4Pipelines.src.metamodel.manifests.manifests import Manifest
from DSL4Pipelines.src.metamodel.artefacts.artefacts import Artefact
from DSL4Pipelines.src.metamodel.artefacts.metrics import Metric
